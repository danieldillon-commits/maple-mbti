let currentQuestion = 0;
let currentJobQuestion = 0;
let selectedMbti = "";
let jobScores = {};

let scores = {
  I: 0,
  E: 0,
  S: 0,
  N: 0,
  T: 0,
  F: 0,
  J: 0,
  P: 0
};

let answerHistory = [];
let jobAnswerHistory = [];

function renderQuestion() {
  document.querySelector(".container").classList.remove("start-screen");

  const q = questions[currentQuestion];

  document.querySelector(".container").innerHTML = `
    <h2>${q.question}</h2>

    <button onclick="selectAnswer('${q.answers[0].type}')">
      ${q.answers[0].text}
    </button>

    <button onclick="selectAnswer('${q.answers[1].type}')">
      ${q.answers[1].text}
    </button>

    <p>${currentQuestion + 1} / 15</p>

    ${currentQuestion > 0 ? `
      <button class="back-btn" onclick="goBackQuestion()">
        이전 질문으로
      </button>
    ` : ""}
  `;
}

function selectAnswer(type) {
  scores[type]++;
  answerHistory.push(type);
  currentQuestion++;

  if (currentQuestion >= questions.length) {
    calculateMBTI();
    return;
  }

  renderQuestion();
}

function goBackQuestion() {
  if (currentQuestion <= 0) return;

  currentQuestion--;

  const lastType = answerHistory.pop();
  if (lastType) {
    scores[lastType]--;
  }

  renderQuestion();
}

function calculateMBTI() {
  const mbti =
    (scores.E >= scores.I ? "E" : "I") +
    (scores.N >= scores.S ? "N" : "S") +
    (scores.F >= scores.T ? "F" : "T") +
    (scores.P >= scores.J ? "P" : "J");

  selectedMbti = mbti;
  currentJobQuestion = 0;
  jobScores = {};
  jobAnswerHistory = [];

  showJobQuestion();
}

function showJobQuestion() {
  const q = jobQuestions[selectedMbti][currentJobQuestion];

  document.querySelector(".container").innerHTML = `
    <h2>${q.question}</h2>

    <button onclick="selectJobAnswer('${q.answers[0].job}')">
      ${q.answers[0].text}
    </button>

    <button onclick="selectJobAnswer('${q.answers[1].job}')">
      ${q.answers[1].text}
    </button>

    <button onclick="selectJobAnswer('${q.answers[2].job}')">
      ${q.answers[2].text}
    </button>

    <p>${currentJobQuestion + 13} / 15</p>

    <button class="back-btn" onclick="goBackJobQuestion()">
      이전 질문으로
    </button>
  `;
}

function selectJobAnswer(job) {
  jobScores[job] = (jobScores[job] || 0) + 1;
  jobAnswerHistory.push(job);
  currentJobQuestion++;

  if (currentJobQuestion >= jobQuestions[selectedMbti].length) {
    showFinalResult();
    return;
  }

  showJobQuestion();
}

function goBackJobQuestion() {
  if (currentJobQuestion > 0) {
    currentJobQuestion--;

    const lastJob = jobAnswerHistory.pop();
    if (lastJob) {
      jobScores[lastJob]--;
    }

    showJobQuestion();
    return;
  }

  currentQuestion = questions.length - 1;

  const lastType = answerHistory.pop();
  if (lastType) {
    scores[lastType]--;
  }

  renderQuestion();
}

function showFinalResult() {
  let finalJob = "";
  let maxScore = -1;

  for (const job in jobScores) {
    if (jobScores[job] > maxScore) {
      maxScore = jobScores[job];
      finalJob = job;
    }
  }

  const allJobs = jobQuestions[selectedMbti][0].answers.map(answer => answer.job);
  const relatedJobs = allJobs.filter(job => job !== finalJob);
  const theme = jobThemes[finalJob] || ["#7c5cff", "#4aa3ff"];

  document.querySelector(".container").innerHTML = `
    <div class="result-card" style="--job-color-1: ${theme[0]}; --job-color-2: ${theme[1]};">
      <p class="result-label">당신의 메BTI 직업은?</p>

      <h1>${finalJob}</h1>

      <h2>${resultTitles[finalJob]}</h2>

      <p class="result-description">
        ${resultDescriptions[finalJob] || ""}
      </p>

      <p class="mbti-text">당신의 메BTI 유형: <strong>${selectedMbti}</strong></p>

      <div class="related-box">
        <p>이런 직업도 잘 맞아요 ✨</p>

        <div class="job-card">${relatedJobs[0]}</div>
        <div class="job-card">${relatedJobs[1]}</div>
      </div>

      <button onclick="copyResult('${finalJob}', '${selectedMbti}')">
        결과 복사하기
      </button>

      <button onclick="location.reload()">
        다시 테스트하기
      </button>
    </div>
  `;
}

function copyResult(job, mbti) {
  const text = "내 메BTI 결과는 " + job + "!\n" + resultTitles[job] + "\n메BTI 유형: " + mbti;
  navigator.clipboard.writeText(text);
  alert("결과가 복사됐어요!");
}

document.getElementById("startBtn").addEventListener("click", renderQuestion);
